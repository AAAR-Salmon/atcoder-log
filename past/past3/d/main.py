digits = [
	'.###..#..###.###.#.#.###.###.###.###.###.',
	'.#.#.##....#...#.#.#.#...#.....#.#.#.#.#.',
	'.#.#..#..###.###.###.###.###...#.###.###.',
	'.#.#..#..#.....#...#...#.#.#...#.#.#...#.',
	'.###.###.###.###...#.###.###...#.###.###.'
]
N = int(input())
s = [input() for _ in range(5)]
ans = ''
for i in range(N):
	for d in range(10):
		if all(s[r][i*4:i*4+4] == digits[r][d*4:d*4+4] for r in range(5)):
			ans += str(d)
			break
print(ans)
